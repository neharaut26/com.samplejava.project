package com.java.project.practices;

public class WhileLoop {

	public static void main(String[] args) {
		int a =1;
		//only while loop
		while(a<=5) {
		System.out.println(a);
		a++;
		}
		
		System.out.println("#############################");
		
		
		//do-while loop
		int i=1;
		do {
			System.out.println(i);
			i++;
		}
	while(i<=10);
	}
	
}
